using Facebook.Unity;
using System.Collections.Generic;
using UnityEngine;

public class FacebookServices : MonoBehaviour
{
    public static FacebookServices Instance;

    private void Awake()
    {
        if(Instance != null && Instance != this)
        {
            Destroy(this);
            return;
        }

        Instance = this;
        DontDestroyOnLoad(this);

        if (FB.IsInitialized == false)
        {
            // Initialize the Facebook SDK
            FB.Init(InitCallback, OnHideUnity);
        }
        else
        {
            // Already initialized, signal an app activation App Event
            FB.ActivateApp();
        }
    }

    private void Update()
    {
        Debug.Log("FB login status " + FB.IsLoggedIn.ToString());
    }

    private void InitCallback()
    {
        if (FB.IsInitialized == true)
        {
            // Signal an app activation App Event
            FB.ActivateApp();
            // Continue with Facebook SDK
            // ...
        }
        else
        {
            Debug.Log("Failed to Initialize the Facebook SDK");
        }
    }

    private void OnHideUnity(bool isGameShown)
    {
        if (isGameShown == false)
        {
            // Pause the game - we will need to hide
            Time.timeScale = 0;
        }
        else
        {
            // Resume the game - we're getting focus again
            Time.timeScale = 1;
        }
    }

    public void LogIn()
    {
        var perms = new List<string>() { "public_profile", "email" };
        FB.LogInWithReadPermissions(perms, AuthCallback);
    }

    public void LogOut()
    {
        if (FB.IsLoggedIn == true)
            FB.LogOut();
        else
            Debug.LogWarning("Not logged into Facebook, so you cannot log out");
    }

    private void AuthCallback(ILoginResult result)
    {
        if (FB.IsLoggedIn == true)
        {
            // AccessToken class will have session details
            var aToken = Facebook.Unity.AccessToken.CurrentAccessToken;
            // Print current access token's User ID
            Debug.Log(aToken.UserId);
            // Print current access token's granted permissions
            foreach (string perm in aToken.Permissions)
            {
                Debug.Log(perm);
            }
        }
        else
        {
            Debug.Log("User cancelled login");
        }
    }
}
